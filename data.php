function getCardById($id) {
    global $cards;
    foreach ($cards as $card) {
        if ($card['id'] == $id) {
            return $card;
        }
    }
    return null;
}

function getCardsByCategory($category) {
    global $cards;
    return array_filter($cards, function($card) use ($category) {
        return $card['category'] === $category;
    });
}

function getAllCategories() {
    global $cards;
    $categories = array_unique(array_column($cards, 'category'));
    return $categories;
}
